def _check_childish_behavior(message: str) -> float:
    """Check if player is being childish (0-1 score) - Layer 1 utility"""
    childish_patterns = [
        r'(asdf|fghj|jkl|lol|lmao|rofl)',
        r'(ha{2,}|he{2,}|hi{2,})',
        r'(stupid|dumb|poop|fart)',
        r'(yo mama|your mom)',
        r'(nonsense|bla{2,})',
        r'(!{3,}|\?{3,}|lalala|nyah)',
    ]
    
    score = 0
    message_lower = message.lower()
    
    for pattern in childish_patterns:
        if re.search(pattern, message_lower):
            score += 0.3
    
    # Check for very short meaningless messages
    if len(message) < 3:
        score += 0.2
    
    return min(1.0, score)


def _apply_world_updates(world_updates: List[Dict], player_state: Dict, player_id: str):
    """Apply world state updates from simulation packet."""
    for update in world_updates:
        update_type = update.get('type', '')
        
        if update_type == 'coins_change':
            amount = update.get('amount', 0)
            player_state['coins'] = player_state.get('coins', 0) + amount
            
            # Emit inventory update to player
            _emit_inventory_update(player_id, player_state)
        
        elif update_type == 'inventory_add':
            item_id = update.get('item_id', '')
            quantity = update.get('quantity', 1)
            
            inventory = player_state.setdefault('inventory', [])
            
            # Check if item already in inventory
            already_has = False
            for entry in inventory:
                if isinstance(entry, dict) and entry.get('item_id') == item_id:
                    entry['quantity'] = entry.get('quantity', 1) + quantity
                    already_has = True
                    break
            
            if not already_has:
                inventory.append({'item_id': item_id, 'quantity': quantity})
            
            _emit_inventory_update(player_id, player_state)
        
        elif update_type == 'hp_change':
            amount = update.get('amount', 0)
            player_state['hp'] = player_state.get('hp', 0) + amount
        
        elif update_type == 'quest_complete':
            quest_id = update.get('quest_id', '')
            completed_quests = player_state.setdefault('completed_quests', [])
            if quest_id and quest_id not in completed_quests:
                completed_quests.append(quest_id)
            
            # Remove from active quests
            active_quests = player_state.get('quests', [])
            player_state['quests'] = [q for q in active_quests if q.get('id') != quest_id]


def _apply_memory_updates(memory_updates: Dict, player_id: str, npc_id: str,
                          conversation_memory: Dict, player_text: str):
    """Apply conversation memory updates from simulation packet."""
    if not memory_updates:
        return
    
    # Initialize memory structure if needed
    if player_id not in conversation_memories:
        conversation_memories[player_id] = {}
    if npc_id not in conversation_memories[player_id]:
        conversation_memories[player_id][npc_id] = {
            'history': [], 'state': 'general', 'childish_warnings': 0
        }
    
    mem = conversation_memories[player_id][npc_id]
    
    # Apply updates
    if memory_updates.get('initialized'):
        mem['history'] = memory_updates.get('history', [])
        mem['state'] = memory_updates.get('state', 'general')
        mem['childish_warnings'] = memory_updates.get('childish_warnings', 0)
    
    if 'childish_warnings' in memory_updates:
        mem['childish_warnings'] = memory_updates['childish_warnings']
    
    if 'state' in memory_updates:
        mem['state'] = memory_updates['state']
    
    if memory_updates.get('history_append'):
        mem['history'].append(memory_updates['history_append'])


