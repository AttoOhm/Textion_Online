def handle_npc_conversation(npc_id, player_text, player_id):
    """Handle NPC conversation using 3-layer system.
    
    Args:
        npc_id: The internal npc ID (e.g. "grim"), not the display name.
        player_text: The player's message.
        player_id: The player's ID.
    
    Returns:
        str: NPC response text (generated by Layer 3)
    """
    npc_data = get_npc_data(npc_id)
    if not npc_data:
        return f"That person doesn't seem to be here."

    # Get player state
    state = get_player_state(player_id)
    if state is None:
        return "You seem lost in thought."

    conversation = npc_data.get('conversation', {})
    npc_name = npc_data.get('name', npc_id)
    greeting = conversation.get('greeting', f"Hello, I'm {npc_name}.")
    farewell_msg = conversation.get('farewell', f"{npc_name} nods and returns to his work.")
    npc_personality = conversation.get('personality', 'friendly')

    player_lower = player_text.lower().strip()

    # Get NPC data for all layers
    shop_data = npc_data.get('shop') or {}
    world_data = npc_data.get('world') or {}
    world_shop = world_data.get('shop') or {}
    shop_items = world_shop.get('items', [])
    world_quests = world_data.get('available_quests', [])
    knowledge = npc_data.get('knowledge', {})
    
    # Extract topic/item/quest lists for Layer 1
    knowledge_topics = list(knowledge.keys()) if knowledge else []
    quest_ids = [q.get('id', '') for q in world_quests] if world_quests else []
    quest_names = [q.get('name', '') for q in world_quests] if world_quests else []
    shop_item_names = [item.get('name', item.get('item_name', '')) for item in shop_items] if shop_items else []
    
    # =========================================================
    # STRICT PIPELINE: Layer 1 → Layer 2 → Layer 3
    # =========================================================
    
    # Get conversation memory
    conversation_memory = conversation_memories.get(player_id, {}).get(npc_id, {
        'history': [], 'state': 'general', 'childish_warnings': 0
    })
    history = conversation_memory.get('history', [])
    
    # Extract NPC capabilities
    npc_capabilities = {
        "Information": {
            "ask_npc": True,
            "ask_location": True,
            "ask_creature": True,
            "ask_item": True,
            "ask_technique": True,
            "ask_shop": True,
            "ask_quest": True,
            "ask_history": True,
            "ask_rumor": True,
            "ask_world": True
        },
        "Quest": {
            "quest_interest": bool(world_quests),
            "quest_information": bool(world_quests),
            "quest_accept": bool(world_quests),
            "quest_decline": bool(world_quests),
            "quest_progress": False,
            "quest_completion": False,
            "quest_finish": bool(world_quests),
            "quest_clarification": bool(world_quests)
        },
        "Commerce": {
            "shop_interest": bool(shop_items),
            "shop_information": bool(shop_items),
            "browse_shop": bool(shop_items),
            "ask_price": bool(shop_items),
            "buy_item": bool(shop_items),
            "sell_item": False,
            "confirm_purchase": bool(shop_items)
        },
        "Training": {
            "training_interest": bool(npc_data.get('skills')),
            "learn_technique": bool(npc_data.get('skills')),
            "improve_technique": bool(npc_data.get('skills')),
            "ask_requirements": bool(npc_data.get('skills'))
        },
        "Services": {
            "heal_request": npc_data.get('type') == 'priest',
            "buff_request": npc_data.get('type') == 'priest',
            "repair_request": npc_data.get('type') == 'blacksmith',
            "identify_request": False,
            "appraisal_request": npc_data.get('type') == 'blacksmith',
            "transport_request": False,
            "lodging_request": False
        }
    }
    
    # =========================================================
    # LAYER 1: LLM Understanding (NO FALLBACKS)
    # =========================================================
    loop = None
    try:
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)
        layer1_result = loop.run_until_complete(
            intent_detector.understand(
                player_message=player_text,
                full_conversation=history,
                npc_capabilities=npc_capabilities,
                npc_knowledge_topics=knowledge_topics,
                npc_quest_ids=quest_ids,
                npc_quest_names=quest_names,
                npc_shop_items=shop_item_names
            )
        )
    except Exception as e:
        # Layer 1 failed - stop processing, raise error
        raise RuntimeError(f"Layer 1 (LLM Understanding) failed: {str(e)}")
    finally:
        if loop:
            loop.close()
    
    # =========================================================
    # STRICT PIPELINE LOGGING
    # =========================================================
    
    # Log Layer 1 raw output
    print("\n" + "="*80)
    print("=== LAYER 1 RAW ===")
    print("="*80)
    print(json.dumps(layer1_result, indent=2))
    print("="*80 + "\n")
    
    # =========================================================
    # LAYER 2: World Simulation (PASS-THROUGH ONLY)
    # =========================================================
    try:
        # Build input for world simulator - pass Layer 1 output unchanged
        world_state = {
            'tick_number': 0,
            'time_of_day': 'day'
        }
        
        simulation_input = {
            'player_message': player_text,
            'conversation_history': history,
            'conversation_memory': conversation_memory,
            'npc_data': npc_data,
            'player_state': state,
            'world_state': world_state,
            'layer1_understanding': layer1_result  # PASSED UNCHANGED - includes subject
        }
        
        # Log what we're sending to Layer 2
        print("\n" + "="*80)
        print("=== SENT TO LAYER 2 ===")
        print("="*80)
        print(json.dumps(simulation_input, indent=2, default=str))
        print("="*80 + "\n")
        
        # Run world simulation
        simulation_result = simulate_world(simulation_input)
        
        # Log Layer 2 output
        print("\n" + "="*80)
        print("=== LAYER 2 OUTPUT ===")
        print("="*80)
        print(json.dumps(simulation_result, indent=2, default=str))
        print("="*80 + "\n")
    except Exception as e:
        # Layer 2 failed - stop processing
        raise RuntimeError(f"Layer 2 (World Simulation) failed: {str(e)}")
    
    # Apply world actions from simulation
    _apply_world_actions(simulation_result.get('world_actions', []), state, player_id, npc_data)
    
    # Apply memory updates from simulation (including pending_purchase)
    _apply_memory_updates(simulation_result.get('memory_updates', {}), player_id, npc_id, 
                         conversation_memory, player_text)
    
    # Build packet for Layer 3 - pass Layer 2 output unchanged
    simulation_packet = simulation_result.copy()
    simulation_packet['npc_name'] = npc_name
    simulation_packet['npc_job'] = world_data.get('job', '')
    
    # =========================================================
    # LAYER 3: Response Generation (PASS-THROUGH ONLY)
    # =========================================================
    try:
        # Log what we're sending to Layer 3
        print("\n" + "="*80)
        print("=== SENT TO LAYER 3 ===")
        print("="*80)
        print(json.dumps(simulation_packet, indent=2, default=str))
        print("="*80 + "\n")
        
        # Generate response - pass packet unchanged
        loop = None
        try:
            loop = asyncio.new_event_loop()
            asyncio.set_event_loop(loop)
            response = loop.run_until_complete(
                response_generator.generate_response(
                    npc=npc_data,
                    player_message=player_text,
                    intent=layer1_result.get('primary_intent', 'general_chat'),
                    packet=simulation_packet,  # PASSED UNCHANGED
                    conversation_history=conversation_memory.get('history', []),
                    reasoning_context=simulation_result  # PASSED UNCHANGED
                )
            )
        finally:
            if loop:
                loop.close()
        
        # Log Layer 3 output
        print("\n" + "="*80)
        print("=== LAYER 3 OUTPUT ===")
        print("="*80)
        print(f"'{response}'")
        print("="*80 + "\n")
        
        # Store NPC response in conversation history
        _append_to_history(player_id, npc_id, player_text, response)
        
        return response
    except Exception as e:
        # Layer 3 failed - stop processing
        raise RuntimeError(f"Layer 3 (Response Generation) failed: {str(e)}")


def _determine_packet_action(simulation_result: Dict) -> str:
    """Determine the packet action from simulation result."""
    world_actions = simulation_result.get('world_actions', [])
    
    if not world_actions:
        # Default action based on conversation state
        conv_state = simulation_result.get('conversation_state', {})
        mode = conv_state.get('mode', 'general')
        
        action_map = {
            'greeting': 'greeting',
            'farewell': 'farewell',
            'quest': 'quest_list_available',
            'shop': 'shop_browse',
            'service': 'service_request',
            'information': 'share_information',
            'conversation': 'general_chat',
            'general': 'general_chat'
        }
        
        return action_map.get(mode, 'general_chat')
    
    # Use the first world action
    first_action = world_actions[0].get('type', 'general_chat')
    
    # Map world actions to packet actions
    # CRITICAL: Only actual success actions map to success packets
    action_map = {
        'offer_quest': 'offer_quest',
        'prepare_quest_offer': 'quest_list_available',
        'complete_purchase': 'shop_purchase_success',  # Only this means success
        'prepare_purchase': 'shop_offer_purchase',      # This is just an offer
        'prepare_service': 'service_request'
    }
    
    return action_map.get(first_action, 'general_chat')


